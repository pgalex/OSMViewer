	/**
	 * Update dailog, showing information about map object
	 *
	 * @param objectToShowInformationAbout map object to show information about
	 * @param objectDrawSettings draw setting of given map object
	 * @throws IllegalArgumentException objectToShowInformationAbout or
	 * objectDrawSettings is null
	 */
	public void showMapObjectInformation(MapObject objectToShowInformationAbout,
					MapObjectDrawSettings objectDrawSettings) throws IllegalArgumentException
	{
		if (objectToShowInformationAbout == null)
		{
			throw new IllegalArgumentException();
		}
		if (objectDrawSettings == null)
		{
			throw new IllegalArgumentException();
		}

		jLabelName.setText(objectDrawSettings.getName());
		jTextAreaDescription.setText(objectDrawSettings.getDescription());
	}

	/**
	 * Clear showing information
	 */
	public void clearInformation()
	{
		jLabelName.setText("");
		jTextAreaDescription.setText("");
	}

